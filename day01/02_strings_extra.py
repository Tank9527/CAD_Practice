# ============================================================
# 练习 7: Netlist 注释清理
# ============================================================
# 清理 netlist 内容：去掉注释行（以 * 或 // 开头）和行内注释（// 后面的部分）
# 去掉空行，返回干净的 netlist

raw_netlist = """
* HSPICE netlist for LDO_TOP
// Generated by Calibre xRC

.subckt LDO_TOP VIN VOUT VSS
R0 VIN net1 10k   // feedback resistor
R1 net1 VSS 20k
C0 VOUT VSS 1p    // load cap
* M0 net1 net2 VDD VSS nmos w=2u l=180n
.ends LDO_TOP

"""

# 期望输出（每行一个元素的列表）:
# [".subckt LDO_TOP VIN VOUT VSS",
#  "R0 VIN net1 10k",
#  "R1 net1 VSS 20k",
#  "C0 VOUT VSS 1p",
#  ".ends LDO_TOP"]

# >>> 在这里写代码 <<<
netlist_pure = []
netlist_split = raw_netlist.split('\n')
for line in netlist_split:
    if line.startswith("//") or line.startswith("*") or line.strip() == "":
        continue  # 跳过注释行和空行
    #else:   #else → 把整行（带 // feedback resistor）加进去了
    #    netlist_pure.append(line.strip()) #加入需要的正常行，并去掉行首尾的空白 
    if "//" in line:
        netlist_pure.append(line.split("//")[0].strip()) # 如果行内有注释，保留注释前的部分，并去掉多余空白
    else:
        netlist_pure.append(line.strip()) # 没有行内注释的正常行，直接去掉首尾空白加入结果列表
print(netlist_pure)        

# ============================================================
# 练习 8: 版本号比较
# ============================================================
# 比较两个 cell 的版本号，判断哪个更新
# 版本格式: "v{major}.{minor}.{patch}" 例如 "v2.1.3"
# 比较规则: 先比 major，再比 minor，最后比 patch

version_pairs = [
    ("v1.2.3", "v1.2.4"),
    ("v2.0.0", "v1.9.9"),
    ("v1.3.0", "v1.3.0"),
    ("v0.10.1", "v0.9.12"),
]

# 对每一对打印: "v1.2.3 < v1.2.4" 或 "v2.0.0 > v1.9.9" 或 "v1.3.0 == v1.3.0"

# >>> 在这里写代码 <<<
for v1,v2 in version_pairs:
    v1_split = list(map(int,v1[1:].split('.'))) # v1[1:]去掉开头的"v"，得到"1.2.3"，再用split('.')分成["1","2","3"]，最后用map(int,...)把每个字符串转换成整数，得到[1,2,3]
    v2_split = list(map(int,v2[1:].split('.')))
    if v1_split < v2_split:   #列表的比较是逐元素比较的，先比较第一个元素，如果相等再比较第二个，以此类推
        print(f"{v1} < {v2}")
    elif v1_split > v2_split:
        print(f"{v1} > {v2}")
    else:
        print(f"{v1} == {v2}")


