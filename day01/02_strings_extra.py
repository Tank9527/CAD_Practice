# ============================================================
# 练习 4: 解析 Spectre 模型参数行
# ============================================================
# 给定一行 Spectre 参数定义，提取所有参数名和值存到字典中
# 注意: 有些参数值带单位，有些不带

param_line = "parameters vth0=0.45 toxe=1.85e-9 u0=320 vsat=1.2e5 rdsw=150"

# 期望输出（字典）:
# {"vth0": 0.45, "toxe": 1.85e-09, "u0": 320.0, "vsat": 120000.0, "rdsw": 150.0}
# 提示: 第一个 token "parameters" 是关键字，要跳过

# >>> 在这里写代码 <<<
expected_dict = {}
param_list = param_line.split()[1:]
for param in param_list:
    key, value = param.split("=") # 将 "vth0=0.45" 分成 ["vth0", "0.45"]
    expected_dict[key] = float(value) #所以value是字符串，需要转换成数字
print(expected_dict)

